package com.atSistemas.antifraude.motor.service.domain;

public class CuentaNegra {
	private String ccc;

	/**
	 * 
	 */
	public CuentaNegra() {
		super();
	}

	/**
	 * @param ccc to set
	 */
	public CuentaNegra(String ccc) {
		super();
		this.ccc = ccc;
	}

	/**
	 * @return the ccc
	 */
	public String getCcc() {
		return ccc;
	}

	/**
	 * @param ccc the ccc to set
	 */
	public void setCcc(String ccc) {
		this.ccc = ccc;
	}
	
}
